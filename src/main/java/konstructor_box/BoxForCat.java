package konstructor_box;

public class BoxForCat {

    int weight;
    int height;
    int depth;

    BoxForCat(int weight, int height, int depth) {
        this.weight = weight;
        this.height = height;
        this.depth = depth;
    }

    public void printBox (){
        System.out.println(weight);
        System.out.println(height);
        System.out.println(depth);
    }
}















