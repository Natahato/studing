package java_types_practics;

public class JavaTypesVariables {

    byte a;
    short b;
    int v;
    long g;
    char j;
    String ff;

    public JavaTypesVariables(byte a, short b, int v, long g, char j, String ff) {
        this.a = a;
        this.b = b;
        this.v = v;
        this.g = g;
        this.j = j;
        this.ff = ff;
    }

    public JavaTypesVariables() {
    }

    public static void main(String[] args) {

        JavaTypesVariables javaTypesVariables = new JavaTypesVariables();



        System.out.println(javaTypesVariables.ff = "Natashulya");
    }
}
